$(".search").on('click', function(e) {
    if ($(".input").val() === "hooks") {
        window.location.href = ("hooks.html")

    } else
    if ($(".input").val() === "lines") {
        window.location.href = ("lines.html")

    } else
    if ($(".input").val() === "engin") {
        window.location.href = ("./engin.html")

    } else
    if ($(".input").val() === "fishing rods") {
        window.location.href = ("./rods.html")

    }

})


